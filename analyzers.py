import google.generativeai as genai
from typing import Optional
import logging

logger = logging.getLogger(__name__)


class NutritionistAnalyzer:
    def __init__(self, api_key: str):
        genai.configure(api_key=api_key)
        # Актуальная модель Gemini 2.5 Flash с поддержкой изображений
        # Используем без префикса models/
        self.model = genai.GenerativeModel('gemini-2.5-flash')

    async def analyze_calories(self, image_data: bytes) -> str:
        """Анализ калорийности блюда по фото"""
        try:
            prompt = """Ты профессиональный нутрициолог. Проанализируй это изображение еды и предоставь:

1. Название блюда/продукта
2. Примерный вес порции (в граммах)
3. Калорийность (ккал)
4. Белки (г)
5. Жиры (г)
6. Углеводы (г)
7. Краткие рекомендации по питанию

Формат ответа должен быть структурированным и понятным. Если на фото нет еды, сообщи об этом."""

            response = self.model.generate_content([prompt, {"mime_type": "image/jpeg", "data": image_data}])
            return response.text
        except Exception as e:
            logger.error(f"Ошибка анализа калорий: {e}")
            return f"Ошибка при анализе изображения: {str(e)}"

    async def analyze_composition(self, image_data: bytes) -> str:
        """Анализ состава продукта по фото этикетки"""
        try:
            prompt = """Ты профессиональный нутрициолог. Проанализируй состав продукта на этикетке и предоставь:

1. Название продукта
2. Основные ингредиенты (в порядке убывания)
3. Пищевая ценность на 100г:
   - Калории
   - Белки
   - Жиры (в т.ч. насыщенные)
   - Углеводы (в т.ч. сахара)
   - Соль/натрий
4. Вредные добавки (E-добавки, консерванты)
5. Оценка полезности (от 1 до 10)
6. Рекомендации: кому подходит, кому стоит избегать

Если на фото нет этикетки с составом, сообщи об этом."""

            response = self.model.generate_content([prompt, {"mime_type": "image/jpeg", "data": image_data}])
            return response.text
        except Exception as e:
            logger.error(f"Ошибка анализа состава: {e}")
            return f"Ошибка при анализе состава: {str(e)}"

    async def analyze_food(self, image_data: bytes) -> str:
        """Общий анализ еды по фото"""
        try:
            prompt = """Ты профессиональный нутрициолог. Проанализируй это блюдо и предоставь:

1. Что изображено на фото
2. Оценка сбалансированности блюда
3. Плюсы этого блюда для здоровья
4. Минусы или потенциальные риски
5. Рекомендации по улучшению (что добавить/убрать)
6. Для какого приёма пищи подходит (завтрак/обед/ужин)
7. Кому особенно рекомендуется, кому стоит избегать

Будь конкретным и практичным в рекомендациях."""

            response = self.model.generate_content([prompt, {"mime_type": "image/jpeg", "data": image_data}])
            return response.text
        except Exception as e:
            logger.error(f"Ошибка анализа еды: {e}")
            return f"Ошибка при анализе еды: {str(e)}"

    async def analyze_medical_tests(self, image_data: bytes) -> str:
        """Анализ медицинских анализов"""
        try:
            prompt = """Ты профессиональный нутрициолог с медицинским образованием. Проанализируй результаты анализов:

1. Определи тип анализа (общий анализ крови, биохимия, гормоны и т.д.)
2. Перечисли показатели и их значения
3. Укажи, какие показатели в норме, какие выше/ниже нормы
4. Объясни, что могут означать отклонения
5. Дай рекомендации по питанию для нормализации показателей:
   - Какие продукты включить
   - Какие продукты ограничить/исключить
   - Режим питания
6. Укажи, требуется ли консультация врача

ВАЖНО: Подчеркни, что это не медицинский диагноз, а рекомендации по питанию. При серьёзных отклонениях необходима консультация врача."""

            response = self.model.generate_content([prompt, {"mime_type": "image/jpeg", "data": image_data}])
            return response.text
        except Exception as e:
            logger.error(f"Ошибка анализа медицинских данных: {e}")
            return f"Ошибка при анализе анализов: {str(e)}"

    async def analyze_stool(self, image_data: bytes) -> str:
        """Анализ фекалий (копрограмма)"""
        try:
            prompt = """Ты профессиональный гастроэнтеролог и нутрициолог. Проанализируй изображение фекалий и предоставь:

1. Оценка по Бристольской шкале стула (тип 1-7)
2. Визуальные характеристики:
   - Цвет (норма: коричневый)
   - Консистенция
   - Форма
   - Наличие видимых включений
3. Возможные причины отклонений от нормы:
   - Проблемы с пищеварением
   - Недостаток/избыток определенных веществ
   - Возможные заболевания
4. Рекомендации по питанию:
   - Какие продукты добавить в рацион
   - Какие продукты исключить/ограничить
   - Режим питания и питьевой режим
   - Пробиотики и пребиотики
5. Когда необходимо обратиться к врачу

ВАЖНО:
- Это предварительная оценка, не заменяющая лабораторную копрограмму
- При наличии крови, черного цвета, сильной боли - немедленно к врачу
- Рекомендуется консультация гастроэнтеролога для точной диагностики

Если на фото не фекалии, вежливо сообщи об этом."""

            response = self.model.generate_content([prompt, {"mime_type": "image/jpeg", "data": image_data}])
            return response.text
        except Exception as e:
            logger.error(f"Ошибка анализа фекалий: {e}")
            return f"Ошибка при анализе: {str(e)}"
